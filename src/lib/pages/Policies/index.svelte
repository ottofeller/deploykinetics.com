        First-party cookies are those set by a website that is being visited by the user, the website displayed in the
        URL window. Third-Party cookies are cookies that are set by a domain other than that of the website being
        visited by the user. If a user visits a website and another entity sets a cookie through that website this would
        be a third-party cookie.
      </p>

      <p>
        Persistent cookies - these cookies remain on a user's device for the period of time specified in the cookie.
        They have activated each time that the user visits the website that created that particular cookie.
      </p>

      <p>
        Session cookie - these cookies allow website operators to link the actions of a user during a browser session. A
        browser session starts when a user opens the browser window and finishes when they close the browser window.
        Session cookies are created temporarily. Once you close the browser, all session cookies are deleted.
      </p>

